SELECT 
    DUAN.MADUAN, 
    DUAN.TENDUAN, 
    SUM(DATEDIFF(MONTH, DUAN.TGBD, DUAN.TGKT)) AS [Số tháng thực hiện], 
    COUNT(DISTINCT PHANCONG.MANV) AS [Số nhân viên], 
    SUM(PHANCONG.SONGAYCONG) AS [Tổng số ngày công đã thực hiện]
FROM 
    DUAN 
    INNER JOIN PHANCONG ON DUAN.MADUAN = PHANCONG.MADUAN 
    INNER JOIN NHANVIEN ON PHANCONG.MANV = NHANVIEN.MANV 
    INNER JOIN CHUCVU ON NHANVIEN.MACHUCVU = CHUCVU.MACHUCVU 
WHERE 
    DUAN.TGKT < '2022-12-31'
GROUP BY 
    DUAN.MADUAN, 
    DUAN.TENDUAN;

--2.
SELECT NV.MANV, HOTENNV, TENCHUCVU, LUONG*SONGAYCONG Lương
FROM CHUCVU CV, NHANVIEN NV, DUAN, PHANCONG PC
WHERE CV.MACHUCVU = NV.MACHUCVU AND DUAN.MADUAN = PC.MADUAN
AND NV.MANV = PC.MANV

--3.
SELECT TOP 1 DUAN.MADUAN, DUAN.TENDUAN
FROM DUAN 
INNER JOIN PHANCONG ON DUAN.MADUAN = PHANCONG.MADUAN 
INNER JOIN NHANVIEN ON PHANCONG.MANV = NHANVIEN.MANV 
INNER JOIN CHUCVU ON NHANVIEN.MACHUCVU = CHUCVU.MACHUCVU 
GROUP BY 
    DUAN.MADUAN, 
    DUAN.TENDUAN
ORDER BY SUM(DATEDIFF(MONTH, DUAN.TGBD, DUAN.TGKT)) ASC, COUNT(DISTINCT PHANCONG.MANV) DESC;


--4.
SELECT NV.MANV, HOTENNV, NGAYSINH, GIOITINH
FROM DUAN, NHANVIEN NV, PHANCONG PC
WHERE DUAN.MADUAN = PC.MADUAN AND NV.MANV = PC.MANV
AND NV.MANV NOT IN(SELECT MANV FROM PHANCONG PC, DUAN 
				   WHERE PC.MADUAN = DUAN.MADUAN AND (YEAR(TGBD) = 2022 OR YEAR(TGKT) = 2022))

--5.
SELECT MADUAN,TENDUAN,TGBD,TGKT
FROM DUAN
WHERE KINHPHI>5000000000

--6.
SELECT NHANVIEN.MANV, HOTENNV, GIOITINH, COUNT(PHANCONG.MADUAN) AS 'So du an'
FROM NHANVIEN LEFT JOIN PHANCONG ON NHANVIEN.MANV = PHANCONG.MANV
WHERE DATEDIFF(YEAR, NGAYSINH, GETDATE()) < 30
GROUP BY NHANVIEN.MANV, HOTENNV, GIOITINH
HAVING COUNT(PHANCONG.MADUAN) <= 2;


